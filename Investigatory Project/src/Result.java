    public Result(int res, String id){
        initComponents();
        result = res;
        ID = id;
        Score.setText(res + "%");
        try{
            Class.forName("java.sql.Driver");
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost/quizdb", "root", "hacker");
            Statement stmt = conn.createStatement();
            String sql = "SELECT * FROM result WHERE ID = '" +ID+ "';";
            ResultSet rs = stmt.executeQuery(sql);
            Object[] newrow = new Object[5];
            newrow[0] = "MAX MARKS";
            rs.next();
            for(int i = 1; i<=4; i++)
                newrow[i] = rs.getString(i + 1);
            DefaultTableModel tm = (DefaultTableModel)ScoreTbl.getModel();
            tm.addRow(newrow);
        }
        catch (Exception e){
            JOptionPane.showMessageDialog(null,"Error!");
        }
    }

